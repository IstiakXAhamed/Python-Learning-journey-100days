@app.route('/game', methods=['GET', 'POST'])
def game():
    if request.method == 'POST':
        guess = request.form.get('guess', type=int)
        difficulty = session.get('difficulty', 'easy')

        if 'actual_num' not in session:
            session['actual_num'] = random.randint(1, 100)
            session['count'] = 10 if difficulty == 'easy' else 5

        actual_num = session.get('actual_num')
        count = session.get('count')

        if guess is not None:
            if guess > actual_num:
                result = 'Too High!'
                count -= 1
            elif guess < actual_num:
                result = 'Too Low!'
                count -= 1
            else:
                result = 'You Guessed it correct!! You Win!!'
                session.pop('actual_num', None)
                session.pop('count', None)
                return render_template('result.html', result=result, winner=winner)

            if count == 0:
                result = f'You Lose!!! The actual number was: {actual_num}'
                session.pop('actual_num', None)
                session.pop('count', None)
                return render_template('result.html', result=result, correct_answer=actual_num, loser=loser)

            session['count'] = count
        else:
            result = None

        return render_template('game.html', result=result, count=count, logo=logo)

    else:
        session.pop('actual_num', None)
        session.pop('count', None)
        return render_template('game.html', result=None, count=None, logo=logo)

